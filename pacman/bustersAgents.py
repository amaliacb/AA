    def chooseAction(self, gameState):

        self.countActions = self.countActions + 1
        self.printInfo(gameState)
        move = Directions.STOP

        # Sacar información relevante del estado del juego
        pacmanPosition = gameState.getPacmanPosition()
        pacmanDirection = gameState.data.agentStates[0].getDirection()
        legal = [a for a in gameState.getLegalPacmanActions()]
        livingGhosts = gameState.getLivingGhosts()

        # Localizar el fantasma más cercano y sacar su index
        minimumDistance = (gameState.data.layout.width * gameState.data.layout.height)
        minimumIndex = -1
        for i in range(1, len(livingGhosts)): # En Living ghosts la primera posición corresponde al pacman así que los fantasmas empiezan en i=1
            if livingGhosts[i]:
                currentDistance = gameState.data.ghostDistances[i-1] 
                if currentDistance < minimumDistance:
                    minimumDistance = currentDistance
                    minimumIndex = i-1
    
        # Calcular la distancia de Pac-Man al fantasma más cercano
        ghostPosition = gameState.getGhostPositions()[minimumIndex]
        xDistance = pacmanPosition[0] - ghostPosition[0]
        yDistance = pacmanPosition[1] - ghostPosition[1]

        # Crear una lista con las posibles acciones de Pac-Man para alcanzar al fantasma
        possibleMoves=[]    
        if(yDistance<0):
            possibleMoves.append(Directions.NORTH)
        if(yDistance>0):
            possibleMoves.append(Directions.SOUTH)
        if(xDistance<0):
            possibleMoves.append(Directions.EAST)
        if(xDistance>0):
            possibleMoves.append(Directions.WEST)

        # Guardar en una variable la direccion por la que ha venido el pacman (la contraria a la que esta yendo)
        if pacmanDirection == Directions.NORTH: comingFrom = Directions.SOUTH
        elif pacmanDirection == Directions.SOUTH: comingFrom = Directions.NORTH
        elif pacmanDirection == Directions.WEST: comingFrom = Directions.EAST
        elif pacmanDirection == Directions.EAST: comingFrom = Directions.WEST
        else: comingFrom = pacmanDirection #Stop

        # Crear una aleatoriedad del 0.2 para cuando no haya un movimiento específico que deva realizar Pac-Man
        p = random.randint(1,10)
        if p <= 2 : arbitrary_move = True
        else: arbitrary_move = False

        moveTowardsNearest = False
        # Si existen acciones legales que acerquen a Pac-Man al fantasma más cercano, realizar una de ellas
        for posMov in possibleMoves:
            if posMov in legal and possibleMoves != comingFrom and not arbitrary_move:
                moveTowardsNearest = True
                move = posMov
        
        # Si Pac-Man solo puede realizar una acción legal (aparte de quedarse parado), se realiza esa acción
        if len(legal)==2:
            legal.remove(Directions.STOP)
            move = legal[0]
        
        # Si entre Pac-Man y el fantasma hay una pared del laberinto,
        # el movimiento de Pac-Man será seguir en la misma dirección de la que viene si es un movimiento legal
        elif not arbitrary_move and not moveTowardsNearest and pacmanDirection in legal:
            move = pacmanDirection
        
        # Si no se pueden tomar ninguna de las decisiones anteriores, Pac-Man se mueve de forma aleatoria
        elif not moveTowardsNearest:
            legal.remove(comingFrom)
            move = legal[random.randint(0, len(legal)-1)]

        return move



